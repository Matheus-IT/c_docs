#include <stdio.h>
#include <stdlib.h>
#include <string.h>


int soma(int n1, int n2)
{
    return n1 + n2;
}


int subtracao(int n1, int n2)
{
    return n1 - n2;
}


int multiplicacao(int n1, int n2)
{
    return n1 * n2;
}


float divisao(int n1, int n2) //Resolver o problema dessa função
{
    return (float)n1 / (float)n2;
}
